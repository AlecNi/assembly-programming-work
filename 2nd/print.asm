STKSEG SEGMENT STACK 
    DW 32 DUP(0) 
STKSEG ENDS 

DATASEG SEGMENT
    NEW_LINE DB 0AH 
    COUNT DB 26
    LOW_ASCII DB "a"
DATASEG ENDS

ASSUME CS:CODESEG,DS:DATASEG,SS:STKSEG 

CODESEG SEGMENT 
MAIN PROC FAR 
    MOV AX,DATASEG 
    MOV DS,AX 
    
    MOV AH,2
L0:
    CMP [COUNT],13
    JGE L1
    JL L2
L1: 
    MOV CX,13
    JMP L3
L2:
    MOV AL,[COUNT]
    MOV CX,0
    MOV CL,AL
L3: 
    MOV AL,[LOW_ASCII]
    MOV DL,AL
    INC AL
    MOV [LOW_ASCII],AL
    INT 21H
    LOOP L3
    
    MOV DL,[NEW_LINE]
    INT 21H
    MOV AL,[COUNT]
    SUB AL,13
    MOV [COUNT],AL
    CMP [COUNT],0
    JG L0

    MOV AX,4C00H 
    INT 21H
    
MAIN ENDP 

CODESEG ENDS 

END MAIN 
