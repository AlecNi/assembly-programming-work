DATASEG SEGMENT
    STRING DB 32 DUP(0)
    COUNT DW 100
    ANS DW 0
DATASEG ENDS

ASSUME CS:CODESEG,DS:DATASEG,SS:STKSEG

CODESEG SEGMENT 
MAIN PROC FAR 
    MOV AX,DATASEG 
    MOV DS,AX 
    
    MOV CX,COUNT
    XOR BX,BX
SUM:
    ADD BX,CX
    LOOP SUM

    MOV [ANS],BX
    XOR BX,BX
    XOR CX,CX
    MOV AX,[ANS]

TRANSF:
    MOV BL,10
    XOR DX,DX
    DIV BX
    MOV BX,CX
    ADD DL,30H
    MOV [STRING+BX],DL
    INC CX
    CMP AX,0
    JNE TRANSF
    
    XOR DX,DX
    MOV AH,2
    MOV BX,CX
OUTPUT:
    DEC BX
    MOV DL,[STRING+BX]
    INT 21H
    LOOP OUTPUT
    
    MOV AX,4C00H 
    INT 21H
    
MAIN ENDP 

CODESEG ENDS 

END MAIN 
